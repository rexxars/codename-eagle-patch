    #[test]
    #[ignore] // needs CE_PRISTINE_143 + CE_GAME_COMMON (+ node), see comment above
    fn provenance_menudll_dll() {
        // menudll.dll is a bundled prebuilt, not byte-patched by this tool; its
        // provenance proof is the generator script: stock 1.43 MENUDLL.DLL run
        // through menudll/make-menudll-patch.js must equal the shipped file.
        let pristine = provenance_dir("CE_PRISTINE_143");
        let common = provenance_dir("CE_GAME_COMMON");
        let dir = std::env::temp_dir()
            .join(format!("cepatch_provenance_menudll_dll_{}", std::process::id()));
        std::fs::create_dir_all(&dir).unwrap();
        let stock = find_file(&pristine, "menudll.dll")
            .unwrap_or_else(|| panic!("MENUDLL.DLL not found in {}", pristine.display()));
        let out = dir.join("menudll.dll");
        let script = std::path::Path::new(env!("CARGO_MANIFEST_DIR"))
            .join("menudll")
            .join("make-menudll-patch.js");

        let status = std::process::Command::new("node")
            .arg(&script)
            .arg(&stock)
            .arg("-o")
            .arg(&out)
            .status()
            .unwrap_or_else(|e| {
                panic!("can't run node (required by {}): {e}", script.display())
            });
        assert!(status.success(), "make-menudll-patch.js failed: {status}");

        let produced = std::fs::read(&out).unwrap();
        let shipped = read_shipped(&common, "menudll.dll");
        std::fs::remove_dir_all(&dir).ok();
        assert_same_bytes("menudll.dll", &produced, &shipped);
    }

