        let mut d = vec![0xa1, 0xd0, 0x3a, 0x42, 0x00, 0xff];
        assert!(edit_state(&d, 0, "a1d03a4200", "e935000000") == St::Patchable);
        let new = unhex("e935000000");
        d[0..5].copy_from_slice(&new);
